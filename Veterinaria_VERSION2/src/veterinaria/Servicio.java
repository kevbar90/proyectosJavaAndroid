package veterinaria;

/**
 *
 * @author Kevin Barrientos
 */
public class Servicio {
    public Servicio(){}
    
    private float consulta=50;
    private float vacuna=30;
    private float peso=20;
    private float dermatologia=75;

    public float getConsulta() {
        return consulta;
    }

    public void setConsulta(float consulta) {
        this.consulta = consulta;
    }

    public float getVacuna() {
        return vacuna;
    }

    public void setVacuna(float vacuna) {
        this.vacuna = vacuna;
    }

    public float getPeso() {
        return peso;
    }

    public void setPeso(float peso) {
        this.peso = peso;
    }

    public float getDermatologia() {
        return dermatologia;
    }

    public void setDermatologia(float dermatologia) {
        this.dermatologia = dermatologia;
    }
    
    
    
    
    
}
