public class Animal {
    public Animal(){}
    
    private String ave="--> AVES";
    private String perro="--> PERROS";
    private String gato="--> GATOS";
    private String reptil="--> REPTILES";
    private String roedor="--> ROEDORES";
    

    public String getAve() {
        return ave;
    }

    public void setAve(String ave) {
        this.ave = ave;
    }

    public String getPerro() {
        return perro;
    }

    public void setPerro(String perro) {
        this.perro = perro;
    }

    public String getGato() {
        return gato;
    }

    public void setGato(String gato) {
        this.gato = gato;
    }

    public String getReptil() {
        return reptil;
    }

    public void setReptil(String reptil) {
        this.reptil = reptil;
    }

    public String getRoedor() {
        return roedor;
    }

    public void setRoedor(String roedor) {
        this.roedor = roedor;
    }

    

}

